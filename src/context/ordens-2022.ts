export const modificationsDate: string[] = [];
orders.orders.map((order) => {
  if (!modificationsDate.includes(order.date)) {
    modificationsDate.push(order.date);
    return order.date;
  }
});

interface Position {
  ativo: string;
  quantidade: number;
  valor: number;
  preço: number;
}

export const positionEvolution: {
  positionDate: string;
  position: Position[];
}[] = [initialPosition];

let index = 1;

modificationsDate.map((date) => {
  const newPositionList: Position[] = [];
  const filteredOrders = orders.orders.filter((order) => order.date === date);
  const lasPosition = positionEvolution[index - 1];
  const currentAssets = lasPosition.position.map((position) => position.ativo);
  const changedAsset = filteredOrders.map((order) => order.asset);
  const unChangedAssets = currentAssets.filter(
    (asset) => !changedAsset.includes(asset)
  );

  if (unChangedAssets.length > 0) {
    unChangedAssets.map((asset) => {
      const unchangedPosition = lasPosition.position.find(
        (position) => position.ativo === asset
      );
      if (unchangedPosition) {
        newPositionList.push({
          ...unchangedPosition,
        });
      }
    });
  }

  filteredOrders.map((order) => {
    const currentPositionIndex = lasPosition.position.findIndex(
      (position) => position.ativo === order.asset
    );

    const currentPositionObject =
      positionEvolution[index - 1].position[currentPositionIndex];

    if (currentPositionObject && order.type === "Venda") {
      const newQtd = currentPositionObject.quantidade - order.quantity;
      if (newQtd > 0) {
        newPositionList.push({
          ativo: order.asset,
          quantidade: newQtd,
          valor: newQtd * currentPositionObject.preço,
          preço: currentPositionObject.preço,
        });
      }
    }

    if (currentPositionObject && order.type === "Compra") {
      const newQtd = currentPositionObject.quantidade + order.quantity;
      const newPrice =
        (currentPositionObject.valor + order.value) /
        (currentPositionObject.quantidade + order.quantity);

      newPositionList.push({
        ativo: order.asset,
        quantidade: newQtd,
        valor: newQtd * currentPositionObject.preço,
        preço: newPrice,
      });
    }

    if (!currentPositionObject && order.type === "Compra") {
      newPositionList.push({
        ativo: order.asset,
        quantidade: order.quantity,
        valor: order.value,
        preço: order.price,
      });
    }
  });
  index++;
  positionEvolution.push({ positionDate: date, position: newPositionList });
});

export const salesOrders = orders.orders
  .filter((order) => order.type === "Venda")
  .map((order) => {
    const currentDate = order.date;
    const currentDateIndex = modificationsDate.indexOf(currentDate);
    let lastDate = modificationsDate[currentDateIndex - 1];

    if (!lastDate) {
      lastDate = "01/01/2022";
    }

    const buyPrice = positionEvolution
      .find((position) => position.positionDate === lastDate)
      ?.position.find((position) => position.ativo === order.asset)?.preço;

    let profitOrLoss = 0;

    if (buyPrice) {
      profitOrLoss = (order.price - buyPrice) * order.quantity;
    }
    return {
      ...order,
      buyPrice,
      profitOrLoss,
    };
  });

export const dateResults = modificationsDate.map((date) => {
  const dateResult = salesOrders
    .filter((order) => order.date === date)
    .reduce((total, order) => total + order.profitOrLoss, 0);

  return {
    date: date,
    dateResult,
  };
});
